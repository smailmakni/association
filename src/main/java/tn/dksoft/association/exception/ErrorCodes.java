package tn.dksoft.association.exception;

public enum ErrorCodes {
	AGENCY_NOT_FOUND(1000), AGENCY_NOT_VALID(1001), AGENCY_ALREADY_IN_USE(1002),

	CENTER_NOT_FOUND(2000), CENTER_NOT_VALID(2001), CENTER_ALREADY_IN_USE(2002),

	ROOM_NOT_FOUND(3000), ROOM_NOT_VALID(3001), ROOM_ALREADY_IN_USE(3002),

	EQUIPMENT_NOT_FOUND(4000), EQUIPMENT_NOT_VALID(4001),

	INTERVENTION_NOT_FOUND(5000), INTERVENTION_NOT_VALID(5001),

	ATTRIBUTION_NOT_FOUND(6000), ATTRIBUTION_NOT_VALID(6001),

	DOCUMENT_NOT_FOUND(7000), DOCUMENT_NOT_VALID(7001),

	REQUETE_NOT_VALID(8000), BAD_CREDENTIALS(9000), EMAIL_UTILISE(9001), USER_NOT_FOUND(10000), USER_NOT_VALID(10001);

	private int code;

	ErrorCodes(int code) {
		this.code = code;
	}

	public int getCode() {
		return code;
	}

}
